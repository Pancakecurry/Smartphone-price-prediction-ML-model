"""
Unit Test Suite for the Data Preprocessing module.

Constructs explicit edge conditions as mock Dataframes to assert the integrity
of Polars vectorized data operations natively without network latencies.
"""
import pytest
import polars as pl
from src.data.data_preprocessing import DataTransformer

@pytest.fixture
def mock_dataset() -> pl.DataFrame:
    """
    Generate an in-memory edge-case dataframe isolated from disk environments.
    
    Includes null pricing targets, heterogeneous string spaces in RAM,
    and missing capacity inputs to accurately test median GroupBy projections.
    """
    return pl.DataFrame({
        "Brand": ["Apple", "Apple", "Apple", "Samsung", "Samsung"],
        "RAM": ["8 GB", "8gb", "16  GB", "12GB", "4  GB"],
        "Camera": ["12 MP", "48 MP", "12MP", "108 MP", "50MP "],
        "Battery": ["3200mAh", None, "4200mAh", "5000mAh", None],
        # Only row index 1 lacks a price. Should drop row [1].
        "Price": [999.99, None, 1399.99, 1199.99, 499.99]
    })


def test_drop_missing_price(mock_dataset):
    """
    Asserts `drop_missing_price()` correctly purges the specific row lacking numeric definition.
    
    Original table: 5 rows (Row idx 1 lacks target Price).
    Resulting table: 4 rows.
    """
    transformer = DataTransformer(mock_dataset)
    clean_df = transformer.drop_missing_price().get_data()
    
    # Prove the missing target row was dropped
    assert len(clean_df) == 4
    # Prove no Nulls remain explicitly in the Price target
    assert clean_df["Price"].null_count() == 0


def test_standardize_features(mock_dataset):
    """
    Asserts `standardize_features()` correctly extracts numeric bases out of strings via RegEx.
    
    e.g. ('8 GB', '8gb', '12  GB') -> Int32 Arrays: (8, 8, 12).
    """
    transformer = DataTransformer(mock_dataset)
    clean_df = transformer.standardize_features().get_data()
    
    # Assert dimension types safely resolved as numeric metrics (Int32)
    assert clean_df["ram_gb"].dtype == pl.Int32
    assert clean_df["battery_mah"].dtype == pl.Int32
    assert clean_df["camera_mp"].dtype == pl.Int32
    
    # Evaluate RegEx scaling against tricky whitespace representations natively
    assert clean_df["ram_gb"][0] == 8
    assert clean_df["ram_gb"][1] == 8
    assert clean_df["ram_gb"][2] == 16


def test_impute_missing_battery(mock_dataset):
    """
    Asserts `impute_missing_battery()` accurately calculates partitioned group Medians.
    """
    transformer = DataTransformer(mock_dataset)
    # Must run standardization first because `battery_mah` (Int32) is generated by it, 
    # instead of doing math on raw `Battery` string rows.
    clean_df = transformer.standardize_features().impute_missing_battery().get_data()
    
    # Row idx 1 (Apple): Missing Battery. Group [3200, 4200]. Median == 3700.0.
    assert clean_df["battery_mah"][1] == 3700.0
    
    # Row idx 4 (Samsung): Missing Battery. Group [5000]. Median == 5000.0.
    assert clean_df["battery_mah"][4] == 5000.0
